import React, { useContext, useState } from 'react';
import { useNavigate } from 'react-router-dom';  // useNavigate for redirection
import axios from 'axios';
import { UserContext } from './UserContext';

const Login = () => {
  const { login } = useContext(UserContext);  // Get the login function from context
  const [username, setUsername] = useState('');
  const [password, setPassword] = useState('');
  const [error, setError] = useState('');
  const navigate = useNavigate();  // Navigation hook for redirect

  const handleLogin = async (e) => {
    e.preventDefault();
    try {
      const response = await axios.post('http://localhost:5000/api/login', { username, password });
      login(response.data);  // Set user state through context
      navigate('/inventory');  // Redirect to inventory after login
      setError('');  // Clear any errors
    } catch (error) {
      setError('Invalid username or password');
    }
  };

  return (
    <div>
      <h2>Login</h2>
      <form onSubmit={handleLogin}>
        <div>
          <label>Username:</label>
          <input type="text" value={username} onChange={(e) => setUsername(e.target.value)} />
        </div>
        <div>
          <label>Password:</label>
          <input type="password" value={password} onChange={(e) => setPassword(e.target.value)} />
        </div>
        <button type="submit">Login</button>
      </form>
      {error && <p style={{ color: 'red' }}>{error}</p>}
    </div>
  );
};

export default Login;
